import UIKit

extension UIColor {
    class func lightOrangeBackgroundColor() -> UIColor {
        return UIColor(red:1, green:0.98, blue:0.95, alpha:1)
    }
    
    class func greyTextColor() -> UIColor {
        return UIColor(red:0.22, green:0.22, blue:0.21, alpha:1)
    }
    
    class func darkOrangeBackgroundColor() -> UIColor {
        return UIColor(red:1, green:0.95, blue:0.9, alpha:1)
    }
    
    class func subOrangeTextColor() -> UIColor {
        return UIColor(red:1, green:0.73, blue:0.47, alpha:1)
    }
    
    class func subGreyTextColor() -> UIColor {
        return UIColor(red:0.43, green:0.43, blue:0.43, alpha:1)
    }
}
